package com.kosmos.brushbreakfast.beans;

public class VerifyRecordModel {
    private String verifyResult;
    private String verifyTime;
    private String roomCardNumber;
    private String ext;
    private String verifyDate;

    public String getVerifyDate() {
        return verifyDate;
    }

    public void setVerifyDate(String verifyDate) {
        this.verifyDate = verifyDate;
    }

    public String getVerifyResult() {
        return verifyResult;
    }

    public void setVerifyResult(String verifyResult) {
        this.verifyResult = verifyResult;
    }

    public String getVerifyTime() {
        return verifyTime;
    }

    public void setVerifyTime(String verifyTime) {
        this.verifyTime = verifyTime;
    }

    public String getRoomCardNumber() {
        return roomCardNumber;
    }

    public void setRoomCardNumber(String roomCardNumber) {
        this.roomCardNumber = roomCardNumber;
    }

    public String getExt() {
        return ext;
    }

    public void setExt(String ext) {
        this.ext = ext;
    }

    @Override
    public String toString() {
        return "VerifyRecordModel{" +
                "verifyResult='" + verifyResult + '\'' +
                ", verifyTime='" + verifyTime + '\'' +
                ", roomCardNumber='" + roomCardNumber + '\'' +
                ", ext='" + ext + '\'' +
                ", verifyDate='" + verifyDate + '\'' +
                '}';
    }
}
