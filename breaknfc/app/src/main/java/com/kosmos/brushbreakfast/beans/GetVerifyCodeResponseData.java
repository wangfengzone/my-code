public class GetVerifyCodeResponseData implements Serializable {
    private String Result;
    private String Msg;
    private String PIN;

    public String getResult() {
        return Result;
    }

    public void setResult(String result) {
        Result = result;
    }

    public String getMsg() {
        return Msg;
    }

    public void setMsg(String msg) {
        Msg = msg;
    }

    public String getPIN() {
        return PIN;
    }

    public void setPIN(String PIN) {
        this.PIN = PIN;
    }

    @Override
    public String toString() {
        return "GetVerifyCodeResponseData{" +
                "Result='" + Result + '\'' +
                ", Msg='" + Msg + '\'' +
                ", PIN='" + PIN + '\'' +
                '}';
    }
}
